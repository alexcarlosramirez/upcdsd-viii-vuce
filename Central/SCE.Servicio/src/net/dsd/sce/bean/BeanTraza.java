package net.dsd.sce.bean;

public class BeanTraza {

	private int trazaId, estadoTraza, de, para;

	public int getTrazaId() {
		return trazaId;
	}

	public void setTrazaId(int trazaId) {
		this.trazaId = trazaId;
	}

	public int getEstadoTraza() {
		return estadoTraza;
	}

	public void setEstadoTraza(int estadoTraza) {
		this.estadoTraza = estadoTraza;
	}

	public int getDe() {
		return de;
	}

	public void setDe(int de) {
		this.de = de;
	}

	public int getPara() {
		return para;
	}

	public void setPara(int para) {
		this.para = para;
	}
}
